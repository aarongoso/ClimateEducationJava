    private void writeQuizScoreToFile(int score) {
    try (BufferedReader reader = new BufferedReader(new FileReader("quiz_score.txt"))) {
        // Read existing scores to determine the next sequence number
        int sequenceNumber = 1;
        String line;
        while ((line = reader.readLine()) != null) {
            if (line.startsWith("Quiz Score ")) {
                sequenceNumber++;
            }
        }

        // Append the new score with the sequence number
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("quiz_score.txt", true))) {
            writer.write("Quiz Score " + sequenceNumber + ": " + score);
            writer.newLine();  // Add a new line for better readability
            System.out.println("Quiz score written to file successfully.");
        }
    } catch (IOException e) {
        System.err.println("Error writing quiz score to file: " + e.getMessage());
    }
}
   
