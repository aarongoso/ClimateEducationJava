  private void calculateQuizScore() {
    int score = 0;

    // Check answers for each question
    if (trueradio1.isSelected()) {
        // User selected "True" for Q1
        score++;
    }

    if (falseradio4.isSelected()) {
        // User selected "False" for Q2
        score++;
    }

    if (trueradio5.isSelected()) {
        // User selected "True" for Q3
        score++;
    }

    if (falseradio8.isSelected()) {
        // User selected "False" for Q4
        score++;
    }

    if (trueradio9.isSelected()) {
        // User selected "True" for Q5
        score++;
    }

    displayQuizResult(score);
}

